class MinStack {
public:
    vector<int> self_stack;
    int self_min_val;

    MinStack() {

    }

    void push(int val) {
        if (self_stack.empty()){
            self_min_val = val;
            self_stack.push_back(0);
        }
        else{
            self_stack.push_back(val-self_min_val);
            self_min_val = min(self_min_val, val);
        }
    }

    void pop() {
        if (self_stack.back() >= 0){
            self_stack.pop_back();
            return;
        }
        self_min_val = self_min_val - self_stack.back();
        self_stack.pop_back();
    }

    int top() {
        if (self_stack.back() >= 0)
            return self_stack.back() + self_min_val;
        return self_min_val;
    }

    int getMin() {
        return self_min_val;
    }
};

int main() {
    MinStack minStack = *new MinStack();
    minStack.push(-2);
    minStack.push(0);
    minStack.push(-3);
    minStack.getMin();   // 返回 -3.
    minStack.pop();
    minStack.top();      // 返回 0.
    minStack.getMin();   // 返回 -2.

}