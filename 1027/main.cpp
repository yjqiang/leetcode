class Solution {
public:
    int longestArithSeqLength(vector<int>& nums) {
        int n = (int)nums.size();
        // dp[i][j] 表示 nums[i,i+1,...,j] 中，以 nums[i] 和 nums[j] 为最后两个元素的等差序列，最长是多少
        vector<vector<int> > dp(n, vector<int>(n));
        unordered_map<int, int> cache;

        int answer = 0;

        for (int i = 0; i < n; i++) {

            for (int j = i + 1; j < n; ++j) {
                // 等差数列的倒数第三个数也可以求出来
                // 如果在 arr[0,1,...i-1] 之间的数值为 wanted 的元素有多个
                // 则仅考虑最右侧的那个（贪心）
                int wanted = 2*nums[i] - nums[j];
                if (cache.find(wanted) == cache.end())
                    dp[i][j] = 2;
                else
                    dp[i][j] = dp[cache[wanted]][i] + 1;

                answer = max(answer, dp[i][j]);
            }
            cache[nums[i]] = i;
        }
        return answer;
    }
};

