class Solution {
public:
    void sortColors(vector<int>& nums) {
        int p0, cur, p2;
        for (p0 = cur = 0, p2 = (int)nums.size()-1; cur <= p2; ){
            if (nums[cur] == 0) {
                swap(nums[p0], nums[cur]);
                ++p0;
                ++cur;
            }
            else if (nums[cur] == 2){
                swap(nums[p2], nums[cur]);
                --p2;
            }
            else
                ++cur;
        }
    }
};

