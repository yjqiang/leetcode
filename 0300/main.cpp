class Solution {
public:
    int lengthOfLIS(vector<int>& nums) {
        // 牌堆们，最终的时候，最大有 nums.size() 个牌堆
        vector<int> dps(nums.size());
        // 有效牌堆的个数
        int cur_length = 0;

        int i;
        int start, end, mid;
        for (i = 0; i < nums.size(); ++i)
            if (cur_length == 0 || nums[i] > dps[cur_length-1]){
                dps[cur_length] = nums[i];
                ++cur_length;
            }
            // 查找所有合适的堆（堆顶元素大于等于 nums[i]）中，堆顶最小的那个堆
            else {
                start = 0;
                end = cur_length;

                while (start < end){
                    mid = start + (end-start)/2;
                    if (dps[mid] >= nums[i])
                        end = mid;
                    else
                        start = mid + 1;

                }

                dps[start] = nums[i];

            }
        return cur_length;
    }
};

