class Solution {
public:
    TreeNode* lowestCommonAncestor(TreeNode* root, TreeNode* p, TreeNode* q) {
        if (root == nullptr || root == p || root == q)
            return root;
        TreeNode *left_result = lowestCommonAncestor(root->left, p, q);
        TreeNode *right_result = lowestCommonAncestor(root->right, p, q);
        if (left_result == nullptr)
            return right_result;
        if (right_result == nullptr)
            return left_result;
        return root;
    }

};

TreeNode *dfs(TreeNode* root, int value){
    if (root == nullptr)
        return nullptr;
    if (root->val == value)
        return root;
    TreeNode *left_answer = dfs(root->left, value);
    if (left_answer != nullptr)
        return left_answer;
    return dfs(root->right, value);
}


